var angka = parseInt(prompt("Masukan Angka: "));

switch(angka) {
    case 1 :
        alert('Anda memasukan angka 1')
        break;
    case 2 :
        alert('Anda memasukan angka 2')
        break;
    case 3 :
        alert('Anda memasukan angka 3')
        break;
    default:
        alert('Angka yang Anda masukan salah')
        break;
        
} 

